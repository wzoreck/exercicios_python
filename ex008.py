# Escreva um programa que leia um valor em metros e
# exiba convertido em centímetros e milímetros
metros = float(input('Digite uma medida em metros: '))
print('\nO valor {} em metros é igual a {} centímetros'.format(metros,(metros*100)))
print('\nO valor {} em metros é igual a {} milimetros'.format(metros,(metros*1000)))
