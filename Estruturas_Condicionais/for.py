# For onde a variável i irá de 0 até 9, repetirá por 10 vezes
for i in range(0, 10):
	print('i = {}'.format(i))

print('\n')

# for onde i sofre decremento de 1 em 1 (-1)
for i in range(10, 0, -1):
	print('i = {}'.format(i))

print('\n')

# incremento maior que 1 por repetição (10 em 10)
for i in range(0, 101, 10):
	print('i = {}'.format(i))
